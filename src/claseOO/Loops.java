package claseOO;

public class Loops {

	public static void main(String[] args) {
		// while, for, switch
		
		///while
		int contador = 1;//VARIABLE DE CONTROL
		
		while (contador <=10) //condicion
		{
			System.out.println("este es un contador "+contador);
			++contador;//incremento del contador
			
		}
		
		//for
		
		for (  contador = 1; contador <=10; contador++)
			System.out.println("este es un for "+contador);

	}
	
	//
	//https://introcs.cs.princeton.edu/java/13flow/
		

}
